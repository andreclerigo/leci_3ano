#!/bin/bash
# --------------------------------------------------------------------------

# Display error message
function ErrorMessage()
{
    echo -e "\e[02;31m${1}\e[0m" 1>&2 ;
}

# --------------------------------------------------------------------------

# Display info message
function InfoMessage()
{
    echo -e "\e[02;34m${1}\e[0m" ;
}

# --------------------------------------------------------------------------

# Display warn message
function WarnMessage()
{
    echo -e "\e[02;33m${1}\e[0m" ;
}

# --------------------------------------------------------------------------


